// task - 1: Create a variables of defferent data type (number,string,boolean,object,array) and log each variable's type using the typeofo operater.

// String
const string = "a";
console.log("string: ", string);
console.log("string typeof: ", typeof string, "\n");

// Number
const number = 12;
console.log("number: ", number);
console.log("number typeof: ", typeof number, "\n");

// Boolean
const boolean = false;
console.log("boolean: ", boolean);
console.log("boolean typeof: ", typeof boolean, "\n");

// Object
const object = { name: "abc", std: 1, rollNo: 10 };
console.log("object: ", object);
console.log("object typeof: ", typeof object, "\n");

// Array
const array = ["abc", "bca", "cab", "bac", "cba", "acb"];
console.log("array: ", array);
console.log("array typeof: ", typeof array, "\n");
