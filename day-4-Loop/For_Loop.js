// Activity:1 - For Loop

// task-1: Write a program to print Numbers from 1 to 10 using loop.

for (let i = 1; i <= 10; i++) {
  console.log("task:1 ", i);
}

console.log("\n");

// task-2: Write a program to print the multiplication table of 5 using loop.

const table = 5;

for (let i = 1; i <= 10; i++) {
  console.log(`task:2 - ${table} * ${i} = ${table * i}`);
}
